import 'package:traqio/features/products/domain/entities/product.dart';

/// Handles JSON <-> Product conversion. Field names here map directly
/// to the Supabase `products` table columns (snake_case).
class ProductModel extends Product {
  const ProductModel({
    required super.id,
    required super.businessId,
    required super.name,
    super.sku,
    super.barcode,
    super.description,
    super.categoryId,
    super.brand,
    required super.unitOfMeasure,
    super.productImage,
    super.activeStatus,
    required super.costPrice,
    required super.sellingPrice,
    super.wholesalePrice,
    super.retailPrice,
    super.taxRate,
    super.discountPrice,
    super.currentStock,
    super.reservedStock,
    super.minimumStock,
    super.reorderLevel,
    super.maximumStock,
    super.openingStock,
    super.openingStockDate,
    super.supplierId,
    super.manufacturer,
    super.countryOfOrigin,
    super.expiryDate,
    super.batchNumber,
    super.serialNumber,
    required super.createdAt,
    required super.updatedAt,
    super.createdBy,
    super.lastModifiedBy,
  });

  factory ProductModel.fromJson(Map<String, dynamic> json) {
    double? asDouble(dynamic v) => v == null ? null : (v as num).toDouble();
    DateTime? asDate(dynamic v) => v == null ? null : DateTime.parse(v as String);

    return ProductModel(
      id: json['id'] as String,
      businessId: json['business_id'] as String,
      name: json['name'] as String,
      sku: json['sku'] as String?,
      barcode: json['barcode'] as String?,
      description: json['description'] as String?,
      categoryId: json['category_id'] as String?,
      brand: json['brand'] as String?,
      unitOfMeasure: json['unit_of_measure'] as String? ?? 'piece',
      productImage: json['product_image'] as String?,
      activeStatus: json['active_status'] as bool? ?? true,
      costPrice: asDouble(json['cost_price']) ?? 0,
      sellingPrice: asDouble(json['selling_price']) ?? 0,
      wholesalePrice: asDouble(json['wholesale_price']),
      retailPrice: asDouble(json['retail_price']),
      taxRate: asDouble(json['tax_rate']),
      discountPrice: asDouble(json['discount_price']),
      currentStock: asDouble(json['current_stock']) ?? 0,
      reservedStock: asDouble(json['reserved_stock']) ?? 0,
      minimumStock: asDouble(json['minimum_stock']) ?? 0,
      reorderLevel: asDouble(json['reorder_level']) ?? 0,
      maximumStock: asDouble(json['maximum_stock']),
      openingStock: asDouble(json['opening_stock']),
      openingStockDate: asDate(json['opening_stock_date']),
      supplierId: json['supplier_id'] as String?,
      manufacturer: json['manufacturer'] as String?,
      countryOfOrigin: json['country_of_origin'] as String?,
      expiryDate: asDate(json['expiry_date']),
      batchNumber: json['batch_number'] as String?,
      serialNumber: json['serial_number'] as String?,
      createdAt: DateTime.parse(json['created_at'] as String),
      updatedAt: DateTime.parse(json['updated_at'] as String),
      createdBy: json['created_by'] as String?,
      lastModifiedBy: json['last_modified_by'] as String?,
    );
  }
Map<String, dynamic> toJson() {
    return {
      'id': id,
      'business_id': businessId,
      'name': name,
      'sku': sku,
      'barcode': barcode,
      'description': description,
      'category_id': categoryId,
      'brand': brand,
      'unit_of_measure': unitOfMeasure,
      'product_image': productImage,
      'active_status': activeStatus,
      'cost_price': costPrice,
      'selling_price': sellingPrice,
      'wholesale_price': wholesalePrice,
      'retail_price': retailPrice,
      'tax_rate': taxRate,
      'discount_price': discountPrice,
      'current_stock': currentStock,
      'reserved_stock': reservedStock,
      'minimum_stock': minimumStock,
      'reorder_level': reorderLevel,
      'maximum_stock': maximumStock,
      'opening_stock': openingStock,
      'opening_stock_date': openingStockDate?.toIso8601String(),
      'supplier_id': supplierId,
      'manufacturer': manufacturer,
      'country_of_origin': countryOfOrigin,
      'expiry_date': expiryDate?.toIso8601String(),
      'batch_number': batchNumber,
      'serial_number': serialNumber,
      'created_at': createdAt.toIso8601String(),
      'updated_at': updatedAt.toIso8601String(),
      'created_by': createdBy,
      'last_modified_by': lastModifiedBy,
    };
  }

  factory ProductModel.fromEntity(Product product) {
    return ProductModel(
      id: product.id,
      businessId: product.businessId,
      name: product.name,
      sku: product.sku,
      barcode: product.barcode,
      description: product.description,
      categoryId: product.categoryId,
      brand: product.brand,
      unitOfMeasure: product.unitOfMeasure,
      productImage: product.productImage,
      activeStatus: product.activeStatus,
      costPrice: product.costPrice,
      sellingPrice: product.sellingPrice,
      wholesalePrice: product.wholesalePrice,
      retailPrice: product.retailPrice,
      taxRate: product.taxRate,
      discountPrice: product.discountPrice,
      currentStock: product.currentStock,
      reservedStock: product.reservedStock,
      minimumStock: product.minimumStock,
      reorderLevel: product.reorderLevel,
      maximumStock: product.maximumStock,
      openingStock: product.openingStock,
      openingStockDate: product.openingStockDate,
      supplierId: product.supplierId,
      manufacturer: product.manufacturer,
      countryOfOrigin: product.countryOfOrigin,
      expiryDate: product.expiryDate,
      batchNumber: product.batchNumber,
      serialNumber: product.serialNumber,
      createdAt: product.createdAt,
      updatedAt: product.updatedAt,
      createdBy: product.createdBy,
      lastModifiedBy: product.lastModifiedBy,
    );
  }
}
